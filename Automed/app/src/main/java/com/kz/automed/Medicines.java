package com.kz.automed;

public class Medicines {

    private String medicne_name;
    private String medicne_dosage;
    private String medicne_time;
    private String patient_day;

    public String getMedicne_name() {
        return medicne_name;
    }

    public void setMedicne_name(String medicne_name) {
        this.medicne_name = medicne_name;
    }

    public String getMedicne_dosage() {
        return medicne_dosage;
    }

    public void setMedicne_dosage(String medicne_dosage) {
        this.medicne_dosage = medicne_dosage;
    }

    public String getMedicne_time() {
        return medicne_time;
    }

    public void setMedicne_time(String medicne_time) {
        this.medicne_time = medicne_time;
    }

    public String getPatient_day() {
        return patient_day;
    }

    public void setPatient_day(String patient_day) {
        this.patient_day = patient_day;
    }

    public Medicines() {
    }
}
